//  --Estrutura de repetição com teste lógico no ínicio--
let c = 1

while (c <= 8)  {
    console.log(`Passo ${c}`);
    c++
} 


//  --Estrutura de repetição com teste lógico no final--
do{
    console.log(`Passo ${c}`);
    c++
} while (c <= 8)


// --Laço de repetição--
for(let c = 1; c <= 8; c++){
    console.log(`Passo ${c}`);
}

